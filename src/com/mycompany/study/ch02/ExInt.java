package com.mycompany.study.ch02;

public class ExInt {

	public static void main(String[] args) {
		
		int var1 = 10;	// 10진수
		int var2 = 012;	// 8진수
		int var3 = 0xa;	// 16진수

		System.out.println("Var1: " + var1);
		System.out.println("Var2: " + var2);
		System.out.println("Var3: " + var3);
		
	}

}
